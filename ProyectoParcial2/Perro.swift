class Perro{
    var origen: String
    var nombre: String
    var tamano: String
    var icono: String
    
    init(origen: String, nombre: String, tamano: String, icono: String) {
        self.origen = origen
        self.nombre = nombre
        self.tamano = tamano
        self.icono = icono
    }
}
